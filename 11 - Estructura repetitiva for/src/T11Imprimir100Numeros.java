
public class T11Imprimir100Numeros {

	public static void main(String[] args) {

		for (int i = 1; i <= 100; i++) {
			System.out.println(" - " + i);
		}

		System.out.println("-------------");

		int f;
		for (f = 1; f <= 10; f++) {
			System.out.print(f + " - ");
		}
		System.out.println();
	}
}
